import Parsing

enum ChunkKind: Character {
    case paren = "(", square = "[", curly = "{", angle = "<"

    init?(closing char: Character) {
        switch char {
        case ")": self = .paren
        case "]": self = .square
        case "}": self = .curly
        case ">": self = .angle
        default: return nil
        }
    }

    var syntaxErrorScore: Int {
        switch self {
        case .paren: return 3
        case .square: return 57
        case .curly: return 1197
        case .angle: return 25137
        }
    }

    var autocompleteScore: Int {
        switch self {
        case .paren: return 1
        case .square: return 2
        case .curly: return 3
        case .angle: return 4
        }
    }

    var opening: Character { rawValue }
    var closing: Character {
        switch self {
        case .paren: return ")"
        case .square: return "]"
        case .curly: return "}"
        case .angle: return ">"
        }
    }
}

private struct SyntaxError: Error {
    let kind: ChunkKind
}

private struct UnexpectedCharacter: Error {
    let character: Character
}

private func finishLine(_ input: Substring) throws -> [ChunkKind] {
    var input = input
    var contexts = [ChunkKind]()

    while let char = input.popFirst() {
        if let opening = ChunkKind(rawValue: char) {
            contexts.append(opening)
        } else if let closing = ChunkKind(closing: char) {
            guard closing == contexts.popLast() else { throw SyntaxError(kind: closing) }
        }
    }
    return contexts.reversed()
}

private func scoreLine(_ remainder: [ChunkKind]) -> Int {
    remainder.lazy
        .map(\.autocompleteScore)
        .reduce(0) { ($0 * 5) + $1 }
}

